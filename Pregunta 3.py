# Número de payasos vendidos
Payasos = int(input("Introduce el número de payasos vendidos: "))
# Número de muñecas vendidas
Muñecas = int(input("Introduce el número de muñecas vendidas: "))
# Peso de cada payaso y muñeca en gramos
Peso_payaso = 112
Peso_muñeca = 75
# Calcular el peso total del paquete
Peso_total = (Peso_payaso * Payasos) + (Peso_muñeca * Muñecas)
# Mostrar el peso total del paquete
print(f"El peso total del paquete es {Peso_total} gramos.")